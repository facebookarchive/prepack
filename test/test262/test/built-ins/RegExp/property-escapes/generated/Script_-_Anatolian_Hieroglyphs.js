const matchSymbols = buildString({
  loneCodePoints: [],
  ranges: [
    [0x014400, 0x014646]
  ]
});
testPropertyEscapes(
  /^\p{Script=Anatolian_Hieroglyphs}+$/u,
  matchSymbols,
  "\\p{Script=Anatolian_Hieroglyphs}"
);
testPropertyEscapes(
  /^\p{Script=Hluw}+$/u,
  matchSymbols,
  "\\p{Script=Hluw}"
);
testPropertyEscapes(
  /^\p{sc=Anatolian_Hieroglyphs}+$/u,
  matchSymbols,
  "\\p{sc=Anatolian_Hieroglyphs}"
);
