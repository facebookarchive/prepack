// monkeypatch the API
$ERROR = function $ERROR(message) {
  throw new Test262Error(message);
};

assert.throws(Test262Error, () => {
  verifyProperty(Object, 'JeanPaulSartre', {});
}, "inexisting property");

assert.throws(Test262Error, () => {
  verifyProperty({}, 'hasOwnProperty', {});
}, "inexisting own property");
